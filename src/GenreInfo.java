
public class GenreInfo {
	private long id;

	private String name;

	private String name_key;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName_key() {
		return name_key;
	}

	public void setName_key(String nameKey) {
		name_key = nameKey;
	}
}
